    public ArrayList<Product> getProductsList(){
      
          
            ArrayList<Product> list = new ArrayList<>();
            String sql = "select * from Products";
            
            Statement stmt;
            ResultSet rs;
            
        try { 
            // Load SQLLITE Database
            Connection con = database.DatabaseConnection();
            
            stmt = con.createStatement();
            rs = stmt.executeQuery(sql);
            Product product;
            
            while(rs.next()){
                product = new Product(rs.getInt("id"), rs.getString("name"),
                        rs.getString("category"), rs.getInt("quantity"),
                        rs.getDouble("price"), rs.getString("image_path"));
                
                list.add(product);
            }
            
            
        } 
        catch (Exception ex) {
             JOptionPane.showMessageDialog(null, "Oops, something went wrong on: " + ex.getMessage());
        }
        productsArray = list;
        return list;
    }

    public void showProductData(int index){
        jSpinner_ID.setValue(getProductsList().get(index).getId());
        txt_name.setText(getProductsList().get(index).getName());
        jComboBox_category.setSelectedItem(getProductsList().get(index).getCategory());
        txt_quantity.setText(getProductsList().get(index).getQuantity().toString());
        txt_price.setText(getProductsList().get(index).getPrice().toString());
        txt_image.setText(getProductsList().get(index).getImage_path());
        displayImage(getProductsList().get(index).getImage_path(), lbl_image);
    }
    
    // create a function to check empty fields
    public boolean checkEmptyFields(){
        String name = txt_name.getText().trim();
        String quantity = txt_quantity.getText().trim();
        String price = txt_price.getText().trim();
        String imagePath = txt_image.getText().trim();
        
        return !((name.equals("") || quantity.equals("") || price.equals("") || imagePath.equals("")));
    }
            
    public void refreshTable(){
         try {
            // Load SQLLITE database
            
            Connection con = database.DatabaseConnection();
            
            Statement stmt = con.createStatement();
            
            String sql = "select * from Products";
            ResultSet rs = stmt.executeQuery(sql);
            
            // code to clear table before viewing
            DefaultTableModel tableModel = (DefaultTableModel)jTable_products.getModel();
            tableModel.setRowCount(0);
            
            while(rs.next()){
            
            //data will be added until finish
            String id = String.valueOf(rs.getInt("id"));
            String name = rs.getString("name");
            String category = rs.getString("category");
            String quantity = String.valueOf(rs.getInt("quantity"));
            String price = String.valueOf(rs.getDouble("price"));
            String image_path = rs.getString("image_path");
            
            
            //string array for store data into jtable
            String tbData[] = {id,name,category,quantity,price,image_path};  
            DefaultTableModel tblModel = (DefaultTableModel)jTable_products.getModel();
            

            //add string array data into jTable
            tblModel.addRow(tbData);
            }
        } 
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Oops, something went wrong on: " + ex.getMessage());
        }
    }
    
