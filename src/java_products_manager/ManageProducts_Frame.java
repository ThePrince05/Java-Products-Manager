    private void jButton_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchActionPerformed
        // TODO add your handling code here:
        if(checkEmptyFields()){ 
            try {
                 int id = (int) jSpinner_ID.getValue();

                // Load SQLLITE database 
                
                Connection con = database.DatabaseConnection();
                Statement stmt = con.createStatement();

                String sql = "select * from Products where id =" + id;

                ResultSet rs = stmt.executeQuery(sql);


                // code to clear table before viewing
                DefaultTableModel tableModel = (DefaultTableModel)jTable_products.getModel();
                tableModel.setRowCount(0);

                if(rs.next()){

                //data will be added until finish
                String recordid = String.valueOf(rs.getInt("id"));
                String name = rs.getString("name");
                String category = rs.getString("category");
                String quantity = String.valueOf(rs.getInt("quantity"));
                String price = String.valueOf(rs.getDouble("price"));
                String image_path = rs.getString("image_path");


                //string array for store data into jtable
                String tbData[] = {recordid,name,category,quantity,price,image_path};  
                DefaultTableModel tblModel = (DefaultTableModel)jTable_products.getModel();


                //add string array data into jTable
                tblModel.addRow(tbData);
                }

                else{
                    JOptionPane.showMessageDialog(null, "Product does not exist.");
                }
            } 
            catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Oops, something went wrong on: " + ex.getMessage());
                }
            } 
        else{
            System.out.println("No empty fields");
        }    
    }//GEN-LAST:event_jButton_SearchActionPerformed

    private void btn_browseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_browseActionPerformed
        // TODO add your handling code here:
        
        JFileChooser filechooser = new JFileChooser();
        filechooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*images",".png","jpg",".jpeg ");
        filechooser.addChoosableFileFilter(filter);
        
        if(filechooser.showSaveDialog(null) == JFileChooser.APPROVE_OPTION){
            File selectedImage = filechooser.getSelectedFile();
            String image_path = selectedImage.getAbsolutePath();
            txt_image.setText(image_path);
            displayImage(image_path, lbl_image);
            System.out.println(image_path);
        }
        else{
            System.out.println("no file selected");
        }
    }//GEN-LAST:event_btn_browseActionPerformed

